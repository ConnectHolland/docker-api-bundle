namespace ConnectHolland\DockerHubApiBundle\Api\Model;

class Error
{
    /**
     * @var string
     */
    protected $detail;

    public function getDetail(): string
    {
        return $this->detail;
    }

    public function setDetail(string $detail): self
    {
        $this->detail = $detail;

        return $this;
    }
}
